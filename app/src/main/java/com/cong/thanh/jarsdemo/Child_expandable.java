package com.cong.thanh.jarsdemo;

public class Child_expandable {
    private int imgHinh;
    private String tenMucchi;

    public Child_expandable(int imgHinh, String tenMucchi) {
        this.imgHinh = imgHinh;
        this.tenMucchi = tenMucchi;
    }

    public int getImgHinh() {
        return imgHinh;
    }

    public void setImgHinh(int imgHinh) {
        this.imgHinh = imgHinh;
    }

    public String getTenMucchi() {
        return tenMucchi;
    }

    public void setTenMucchi(String tenMucchi) {
        this.tenMucchi = tenMucchi;
    }
}
